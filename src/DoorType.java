/**
 * An enum that holds the different types of doors.
 * @author William King
 */
public enum DoorType {
	/** A yellow door. It can be opened with a yellow key. */
    YELLOW,
    
    /** An orange door. It can be opened with an orange key. */
    ORANGE,
    
    /** A purple door. It can be opened with a purple key. */
    PURPLE,
    
    /** A token door. It can be opened with a specified number of tokens.*/
    TOKEN
}
